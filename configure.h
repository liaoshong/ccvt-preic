#ifndef CONFIGURE_H
#define CONFIGURE_H

#define DIMENSION			3
#define CCDT_INIT			1

/*******************************************************
  Explanations of the configure options listed above:

  DIMENSION		The dimension of particles you
                	want to generate. Currently, only
			2-D and 3-D are supported.

  CCDT_INIT		Switch on/off the CCDT initialization
			option. 1: switch on; otherwise: switch
			off. Recommend to switch on it to
			accelerate the computation.
********************************************************/

#endif
